int			ft_itoa_buf(char *s, long i)
{
	long		j;
	long		len;
	long		idx;

	if (i == 0)
	{
		*s = '0';
		return (1);
	}
	len = 1;
	j = i;
	while (j > 9)
	{
		len++;
		j /= 10;
	}
	idx = len - 1;
	while (i)
	{
		s[idx--] = (i % 10) + '0';
		i /= 10;
	}
	return ((int)(len));
}
